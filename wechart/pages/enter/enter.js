  /**
   * 页面的初始数据
   */
  data: {
    date:""
  },

  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    let date = this.formatDate(new Date());
    this.setData({
      date:date
    })
  },
  bindDateChange(e) {
    let date = e.detail.value;
    this.setData({
      date: date
    })
  },
  formatDate: function (date) {
    var y = date.getFullYear();
    var m = date.getMonth() + 1;
    m = m < 10 ? '0' + m : m;
    var d = date.getDate();
    d = d < 10 ? ('0' + d) : d;
    return y + '-' + m + '-' + d;
  },
